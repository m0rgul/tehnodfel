import mongoose from 'mongoose';
const Schema = mongoose.Schema;

let userSchema = new Schema({
  firstName: {
    type: String,
    required: true
  },
  lastName: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true,
    unique: true
  },
  password: {
    type: String,
    required: true
  },
  isAdmin: {
    type: Boolean,
    default: false
  }
});

let User = mongoose.model('User', userSchema);

export default User;
